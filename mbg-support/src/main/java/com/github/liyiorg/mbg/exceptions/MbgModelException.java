package com.github.liyiorg.mbg.exceptions;

public class MbgModelException extends MbgException {

	private static final long serialVersionUID = 3570142460976540675L;

	public MbgModelException() {
		super();
	}

	public MbgModelException(String message, Throwable cause) {
		super(message, cause);
	}

	public MbgModelException(String message) {
		super(message);
	}

	public MbgModelException(Throwable cause) {
		super(cause);
	}

}
