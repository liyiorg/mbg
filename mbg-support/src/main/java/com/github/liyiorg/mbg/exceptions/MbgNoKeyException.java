package com.github.liyiorg.mbg.exceptions;

public class MbgNoKeyException extends MbgException {

	private static final long serialVersionUID = -6994353941424702174L;

	public MbgNoKeyException() {
		super();
	}

	public MbgNoKeyException(String message, Throwable cause) {
		super(message, cause);
	}

	public MbgNoKeyException(String message) {
		super(message);
	}

	public MbgNoKeyException(Throwable cause) {
		super(cause);
	}

}
